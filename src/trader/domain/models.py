class HealthStatus(BaseModel):
    """Aggregated health status of the trading system."""

    model_config = ConfigDict(frozen=True)

    timestamp: datetime = Field(default_factory=_now_utc)
    overall: str  # healthy / degraded / unhealthy

    postgres: bool = False
    redis: bool = False
    bybit_rest: bool = False
    bybit_ws: bool = False
    model_fresh: bool = False
    features_fresh: bool = False

    system_status: SystemStatus = SystemStatus.STOPPED
    trading_mode: TradingMode = TradingMode.TESTNET

    # Per-component latencies (ms)
    postgres_latency_ms: float | None = None
    redis_latency_ms: float | None = None
    bybit_rest_latency_ms: float | None = None

    # Any health check messages
    messages: list[str] = Field(default_factory=list)

    @property
    def is_healthy(self) -> bool:
        return self.overall == "healthy"


