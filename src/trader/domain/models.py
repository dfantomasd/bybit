class OrderIntent(BaseModel):
    """Fully-specified order parameters ready for submission to the exchange."""

    model_config = ConfigDict(frozen=True)

    intent_id: uuid.UUID = Field(default_factory=uuid.uuid4)
    decision_id: uuid.UUID
    proposal_id: uuid.UUID

    # Bybit-specific fields
    symbol: str
    market_type: MarketType
    side: OrderSide
    order_type: OrderType

    qty: Decimal = Field(..., gt=Decimal(0))
    price: Decimal | None = None  # Required for LIMIT

    # Idempotency key sent to exchange (max 36 chars, alphanumeric + _-)
    order_link_id: str = Field(..., min_length=1, max_length=36)

    # Risk management orders
    take_profit: Decimal | None = None
    stop_loss: Decimal | None = None
    tp_order_type: OrderType = OrderType.LIMIT
    sl_order_type: OrderType = OrderType.MARKET

    # Reduce-only flag
    reduce_only: bool = False

    # Time-in-force (GTC, IOC, FOK, PostOnly)
    time_in_force: str = "GTC"

    created_at: datetime = Field(default_factory=_now_utc)

    @field_validator("order_link_id")
    @classmethod
    def validate_order_link_id(cls, v: str) -> str:
        if not _ORDER_LINK_ID_RE.match(v):
            raise ValueError("order_link_id must be 1-36 alphanumeric chars (plus _ and -)")
        return v

    @model_validator(mode="after")
    def price_required_for_limit(self) -> OrderIntent:
        if self.order_type == OrderType.LIMIT and self.price is None:
            raise ValueError("price is required for LIMIT orders")
        return self


