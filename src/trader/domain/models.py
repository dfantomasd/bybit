class TradeProposal(BaseModel):
    """Signal generated by a strategy or RL model requesting a trade."""

    model_config = ConfigDict(frozen=True)

    proposal_id: uuid.UUID = Field(default_factory=uuid.uuid4)
    strategy_id: str = Field(..., min_length=1, max_length=64)
    symbol: str
    market_type: MarketType
    side: OrderSide
    timestamp: datetime = Field(default_factory=_now_utc)

    # Sizing
    requested_qty: Decimal = Field(..., gt=Decimal(0))
    requested_notional_usd: Decimal | None = None

    # Price targets
    entry_price: Decimal | None = None  # None → market order
    take_profit: Decimal | None = None
    stop_loss: Decimal | None = None

    # Model confidence [0, 1]
    confidence: float = Field(..., ge=0.0, le=1.0)

    # Risk / reward estimate
    expected_return: float | None = None
    expected_risk: float | None = None

    # Regime at time of proposal
    regime: MarketRegime = MarketRegime.UNCERTAIN

    # Feature vector that drove this proposal
    feature_id: uuid.UUID | None = None

    # Human-readable rationale
    rationale: str = ""

    @field_validator("symbol")
    @classmethod
    def symbol_uppercase(cls, v: str) -> str:
        return v.upper()

    @model_validator(mode="after")
    def sl_on_correct_side(self) -> TradeProposal:
        if self.stop_loss is not None and self.entry_price is not None:
            if self.side == OrderSide.BUY and self.stop_loss >= self.entry_price:
                raise ValueError("stop_loss must be below entry_price for BUY")
            if self.side == OrderSide.SELL and self.stop_loss <= self.entry_price:
                raise ValueError("stop_loss must be above entry_price for SELL")
        return self


class RiskDecision(BaseModel):
    """Risk Manager's verdict on a TradeProposal."""

    model_config = ConfigDict(frozen=True)

    decision_id: uuid.UUID = Field(default_factory=uuid.uuid4)
    proposal_id: uuid.UUID
    timestamp: datetime = Field(default_factory=_now_utc)

    status: RiskDecisionStatus

    # Approved quantity (may differ from requested)
    approved_qty: Decimal | None = None
    approved_notional_usd: Decimal | None = None

    # If RESIZED, original requested qty
    original_qty: Decimal | None = None

    # Human-readable reason (especially on REJECTED)
    reason: str = ""

    # Which risk rule triggered the decision
    triggered_rules: list[str] = Field(default_factory=list)

    # Portfolio-level metrics at time of decision
    portfolio_heat: float | None = None
    current_drawdown_pct: float | None = None
    open_positions_count: int | None = None

    @model_validator(mode="after")
    def approved_qty_required_when_approved(self) -> RiskDecision:
        if self.status in (RiskDecisionStatus.APPROVED, RiskDecisionStatus.RESIZED):
            if self.approved_qty is None:
                raise ValueError("approved_qty is required when status is APPROVED or RESIZED")
            if self.approved_qty <= 0:
                raise ValueError("approved_qty must be positive")
        return self


